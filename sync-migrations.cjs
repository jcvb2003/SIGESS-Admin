const fs = require('fs');
const path = require('path');

const migrationsDir = path.resolve(__dirname, '../Web/supabase/migrations');
const outputFile = path.resolve(__dirname, 'supabase/functions/_shared/migrations_bundle.ts');

if (!fs.existsSync(migrationsDir)) {
  if (fs.existsSync(outputFile)) {
    console.log('⚠️  Migrations dir não encontrado (Ambiente CI?), mas o bundle já existe. Pulando...');
    process.exit(0);
  }
  console.error(`Migrations directory not found at: ${migrationsDir}`);
  process.exit(1);
}

const files = fs.readdirSync(migrationsDir)
  .filter(f => f.endsWith('.sql'))
  .sort();
const migrations = {};

for (const file of files) {
  const content = fs.readFileSync(path.join(migrationsDir, file), 'utf8');
  // Strip .sql extension for key
  const key = file.replace(/\.sql$/, '');
  migrations[key] = content;
}

const fileContent = `// AUTO-GENERATED FILE. DO NOT EDIT DIRECTLY.
// Generated by sync-migrations.cjs

export const migrationsBundle = ${JSON.stringify(migrations, null, 2)};
`;

fs.writeFileSync(outputFile, fileContent, 'utf8');
console.log(`Successfully bundled ${files.length} migrations to ${outputFile}`);
